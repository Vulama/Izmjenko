package com.example.manjaro.izmjenko;

public class ClassInfo {
    public String subject="";
    public String startTime="";
    public String endTime="";
    public String header="";

    public ClassInfo(String argSubject,String argStartTime,String argEndTime,String argHeader){
        subject=argSubject;
        startTime=argStartTime;
        endTime=argEndTime;
        header=argHeader;
    }
}
